import type { Metadata } from "next";
import { Pacifico, Quicksand, JetBrains_Mono } from "next/font/google";
import "./globals.css";

// Font optimization with display: "swap" to prevent hydration errors
// and ensure consistent font loading between server and client
const displayFont = Pacifico({
  variable: "--font-display",
  subsets: ["latin"],
  weight: "400", // Pacifico only has 400 weight
  display: "swap",
});

const bodyFont = Quicksand({
  variable: "--font-body",
  subsets: ["latin"],
  weight: ["300", "400", "500", "600", "700"],
  display: "swap",
});

const monoFont = JetBrains_Mono({
  variable: "--font-mono",
  subsets: ["latin"],
  display: "swap",
});

export const metadata: Metadata = {
  title: "MLBoss",
  description: "Generated by create next app",
};

export default function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  return (
    <html lang="en" suppressHydrationWarning>
      <body
        className={`${displayFont.variable} ${bodyFont.variable} ${monoFont.variable} antialiased`}
      >
        {children}
      </body>
    </html>
  );
}
